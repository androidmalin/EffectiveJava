    public boolean isCompleteTree2(TreeNode root) {
        if (root == null) return true;
        int totalNum = countNodeNumber(root);
        return numberIndex(1, totalNum, root);
    }

    private boolean numberIndex(int index, int total, TreeNode root) {
        if (root == null) return true;
        if (index > total) return false;
        boolean left = numberIndex(2 * index, total, root.left);
        boolean right = numberIndex(2 * index + 1, total, root.right);
        return left && right;
    }

    private int countNodeNumber(TreeNode root) {
        if (root == null) return 0;
        return 1 + countNodeNumber(root.left) + countNodeNumber(root.right);
    }
